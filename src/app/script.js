function rightArrow() { //mouseover, turn arrow black
  var right = document.getElementById('right');
  right.src = 'assets/img/right.png';
}
function resetRight() { //mouseoff, turn arrow gray
  var right = document.getElementById('right');
  right.src = 'assets/img/rightgray.png';
}
function leftArrow() { //mouseover, turn arrow black
  var left = document.getElementById('left');
  left.src = 'assets/img/left.png';
}
function resetLeft() { //mouseoff, turn arrow gray
  left.src = 'assets/img/leftgray.png';
}
